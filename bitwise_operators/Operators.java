// package bitwise_operators;

public class Operators {
    public static void main(String[] args) {
        System.out.print("Binary and operator (&) of 10 & 5 is : ");
        System.out.println(10 & 5);

        System.out.print("Binary or operator (|) of 10 & 5 is : ");
        System.out.println(10 | 5);

        System.out.print("Binary XOR operator (^) of 10 & 5 is : ");
        System.out.println(10 ^ 5);

        System.out.print("Binary ones complement of 10 is : ");
        System.out.println(~10);

        System.out.print("Binary left shift operator of 10 by 2 is : ");
        System.out.println(10 << 2);

        System.out.print("Binary right shift operator of 10 by 2 is : ");
        System.out.println(10 >> 2);


    }
}
